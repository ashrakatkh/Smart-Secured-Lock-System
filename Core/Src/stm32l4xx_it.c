/**
  * @brief This function handles USART1 global interrupt.
  */


//uint8_t x = 'Y';

void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	
//	HAL_UART_Receive (&huart1, password, 5, 100);
//	flag = 1;
//	
	HAL_UART_Receive (&huart1, &password[count], 1, 100);
	//HAL_UART_Transmit (&huart1, password, 5, 100);
	count++;
	if(count == 5){
	//HAL_UART_Transmit (&huart1, password, 5, 100);
	//HAL_UART_Transmit (&huart1, &x, 1, 100);
	//flag = 1;
		for (int i = 0; i < 5; i++)
			{
				if (password[i] != correct_password [i] )
				{ 
					correct = 0;
					password[i] = NULL;
				}
			}
			if ( correct)
			{
				HAL_UART_Transmit (&huart1, right, 5, 100);
			}
			else{
				HAL_UART_Transmit (&huart1, wrong, 5, 100);
				correct = 1;
				wrong_count ++;
				if(wrong_count==3){
				
					HAL_UART_Transmit (&huart1, alert, 6, 100);
					wrong_count = 0;
				
				}
			}
	count = 0;
	}
	
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

